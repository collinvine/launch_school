let weather = "fog";

switch (weather) {
  case "sunny":
    console.log("It's a beautiful day out there.");
    break;
  case "rainy":
    console.log("Get that umbrella");
    break;
  case "snow":
    console.log("Get yer toque!");
    break;
  default:
    console.log("Who knows, but better stay inside");
}
